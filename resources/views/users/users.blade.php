@section('js')
  <script type="text/javascript">
    function hapus(id,nama){
        bootbox.confirm({
          message: "Apakah anda ingin menghapus data user : "+nama+" ?",
          buttons: {
              confirm: {
                  label: 'Hapus',
                  className: 'btn-success'
              },
              cancel: {
                  label: 'Kembali',
                  className: 'btn-danger'
              }
          },
          callback: function (result) {
              if (result) {
                $('#formHapus').attr('action', '{{url('users')}}/'+id);
                $('#formHapus').submit();
              }
          }
      });
    }
  </script>
